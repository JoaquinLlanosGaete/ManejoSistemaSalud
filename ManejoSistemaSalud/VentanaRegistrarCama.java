    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
        if(numeroCama.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ingrese un número de cama.", "ALERTA", JOptionPane.WARNING_MESSAGE);
        }
        if(tipoCama.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ingrese un tipo de cama.", "ALERTA", JOptionPane.WARNING_MESSAGE);
        }
        else{
            int numCama = Integer.parseInt(numeroCama.getText());
            String tipo = tipoCama.getText().toLowerCase();
            if(!(tipo.equals("general") || tipo.equals("intermedia") || tipo.equals("intensiva"))){
                JOptionPane.showMessageDialog(this, "El tipo no es válido, debe ser: general, intermedia o intensiva", "ALERTA", JOptionPane.WARNING_MESSAGE);
            }
            else{
                if(controlador.existeCama(numCama)){
                    JOptionPane.showMessageDialog(this, "La cama " + numCama + " ya existe.", "ALERTA", JOptionPane.WARNING_MESSAGE);
                }
                else{
                    controlador.registrarCama(numCama, tipo);
                    JOptionPane.showMessageDialog(this, "La cama fue registrada con éxito", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                    controlador.grabarDato(controlador.getDirectorio("cama"), numeroCama.getText()+";"+tipo+";"+true+"\n");
                    controlador.mostrarVentanaPrincipal();
                    this.dispose();
                }
            }
        }
    }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(this, "Ingrese un número válido para la cama.", "Error de formato", JOptionPane.ERROR_MESSAGE);
    } catch (CsvValidationException e) {
        throw new RuntimeException(e);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

